print(a == b) # This will give you False

# In the example below, we use the != operator to check if two values are not equal:

a = 5
b = 10
print(a != b) # This will give you True

# In the example below, we use the > operator to check if one value is greater than another:

a = 5
b = 10
print(a > b) # This will give you False

# In the example below, we use the < operator to check if one value is less than another:

a = 5
b = 10
print(a < b) # This will give you True

# In the example below, we use the >= operator to check if one value is greater than or equal to another:

a = 5
b = 10
print(a >= b) # This will give you False

# In the example below, we use the <= operator to check if one value is less than or equal to another:

a = 5
b = 10
print(a <= b) # This will give you True

# In the example below, we use the and operator to check if two conditions are true:

a = 5
b = 10
print(a < 5 and 10 > b) # This will give you False

# In the example below, we use the or operator to check if one of two conditions is true:

a = 5
b = 10
print(a < 5 or 10 > b) # This will give you True

# In the example below, we use the not operator to check if a condition is false:

a = 5
b = 10
print(not(a < 5 and 10 > b)) # This will give you True

# Python Assignment Operators
# Assignment operators are used to assign values to variables.
# Example
a = 5
b = 10
c = a + b
print(c) # This will give you 15

# In the example below, we use the += operator to add a value to a variable:

a = 5   
b = 10
a += b
print(a) # This will give you 15

# In the example below, we use the -= operator to subtract a value from a variable:

a = 5
b = 10
a -= b
print(a) # This will give you -5

# In the example below, we use the *= operator to multiply a value to a variable:

a = 5
b = 10
a *= b
print(a) # This will give you 50

# In the example below, we use the /= operator to divide a value to a variable:

a = 5
b = 10
a /= b
print(a) # This will give you 0.5

# In the example below, we use the %= operator to get the remainder of a division to a variable:

a = 5
b = 10
a %= b
print(a) # This will give you 5

# In the example below, we use the **= operator to raise a value to a power to a variable:

a = 5
b = 10
a **= b
print(a) # This will give you 9765625

